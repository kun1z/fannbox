ui in_range(const u16 n)
{
    if (n > 2 && n < 15) return 1;
    else if (n > 15258 && n < 18310) return 1;
    else if (n > 57220 && n < 61036) return 1;
    else return 0;
}
//----------------------------------------------------------------------------------------------------------------------
u16 rng_word(void)
{
    static ui p = 1000;
    static u16 block[32] = { 0 };

    if (p >= 32)
    {
        p = 0;
        blake2b_mod(block, block);
    }

    return block[p++];
}
//----------------------------------------------------------------------------------------------------------------------
u32 tick(void)
{
    struct timespec now;
    clock_gettime(CLOCK_MONOTONIC, &now);
    return (now.tv_sec * 1000UL) + (now.tv_nsec / 1000000UL);
}
